/**
 * 보기(선택지) 제작 — 고난도·균형·단일 정답
 * A4 변형세트 패턴 + 출제위원 품질 원칙
 */

/** 모든 객관식에 공통으로 넣는 핵심 원칙 */
export function choiceCraftCommonRules(): string {
  return `보기 제작 핵심 (필수 · 고난도 단일정답):
목표: 학생이 보기만 보고는 정·오를 가르지 못하게 하되, 지문을 정확히 이해하면 정답은 딱 하나.

1) 정답만 눈에 띄지 않게
- 정답만 길거나·구체적이거나·세련된 표현 금지.
- 정답만 지문 핵심어를 그대로 반복 금지.
- 길이·문장구조·어휘수준·구체성 정도를 5개 비슷하게.
- 정답 번호는 1~5에 고르게 (특정 번호 편중 금지).

2) 황당·즉시제거 오답 금지
- 지문 무관·상식상 명백히 틀림·과도한 과장 금지.
- 오답도 지문의 실제 문장·개념을 근거로 만들 것.
- 키워드 일치만으로 고를 수 없게 할 것.
- 각 오답 = 지문 일부와 연결 + 핵심 관계만 미세하게 틀림.

3) 오답 변형 기법 (노골적 반의어·not만 붙이기 금지)
- 주체↔대상 / 원인↔결과 / 조건↔결과 / 수단↔목적
- 전체 주장↔세부 사례 / 필자 주장↔글 속 통념
- 긍정·부정 방향을 은근히 변경
- 정도 미세 과장·약화 (always/only/entirely/necessarily 확대, some/often/may/tend to 축소) — 극단어를 ‘오답 표시’처럼 남용 금지
- 시간 선후·비교 대상 변경
- 지문 두 개념을 결합하되 관계만 잘못 설정
- 맞는 세부 둘을 연결해 틀린 결론 만들기

4) 정답도 원문 복사 금지 → 정확한 paraphrase (논리관계 이해 필요)
- 같은 지문으로 여러 문항을 만들 때 문항마다 동의어·유의어 세트를 바꿔 쓸 것 (동일 핵심어 반복 금지).
- 오답에는 미세한 반의·대비(antonym/contrast)를 활용하되 not만 붙인 노골적 반전 금지.

5) 난이도 균형
- 5개 모두 처음엔 어느 정도 가능해 보이게.
- 최소 2개는 정답과 매우 유사한 ‘강한 오답’.
- 나머지 오답도 주변 정보와 연결해 쉽게 안 지워지게.
- 품사·구조 통일 (명사구형끼리 / 완전문장끼리). 혼합 금지.
- 선택지 간 의미 중복·포함 관계 금지.

6) 어려움은 지문 이해에서
- 생소 어휘·과도한 복문으로 억지 난이도 주지 말 것.
- 한 문장만 보고 풀 수 없게 글 전체 흐름 반영.

금지: 정답만 원문 반복·정답만 김·황당 오답·지문 없는 새 소재·번역투·복수 정답·배경지식 필수·키워드만으로 풀림.

최종 자가검수 (출력 전):
① 다섯 보기 모두 그럴듯한가 ② 정답만 띄는 단서 없는가 ③ 강한 오답 ≥2인가
④ 각 오답의 변형 근거를 말할 수 있는가 ⑤ 정답이 지문 전체로 입증되는가
⑥ 정답 하나인가 ⑦ 길이·구조 균형 ⑧ 정밀 독해 필요한가 ⑨ 작위·억지 없는가 ⑩ 내신 논란 없을 만큼 명확한가
미충족 시 보기 재작성.`;
}

/** 해설에 넣을 형식 */
export function choiceExplanationRules(): string {
  return `해설(explanation) 한글 형식:
- 정답 번호 + 정답 근거(지문 어느 논리인지)
- 각 오답: (1) 왜 매력적으로 보이는지 (2) 최종적으로 왜 틀리는지
- “정답이 ③이기 때문” 순환 설명 금지.
- voice/CASE 등 영어 은어 금지.`;
}

export function topicChoiceCraft(en: boolean): string {
  return `주제 보기:
- 정답: 핵심 대상+핵심 관계 모두 (${en ? "영어 명사구/how절" : "한국어 구"}).
- 강한 오답①: 대상은 맞지만 관계가 틀림.
- 강한 오답②: 관계는 맞지만 범위가 너무 좁거나 넓음.
- 그 외: 도입부만 / 사례만 일반화.
- 정답만 과도하게 추상·멋진 표현 금지.`;
}

export function titleChoiceCraft(en: boolean): string {
  return `제목 보기:
- 정답: 중심 주장·관점 함축. 원문 문장 복사 금지. ${en ? "Title Case" : "자연스러운 한국어"}.
- 강한 오답: 키워드는 같되 메시지 빗나감 / 범위 오류.
- 정답만 화려·은유적이지 않게. 오답과 톤 맞출 것.`;
}

export function summaryChoiceCraft(en: boolean): string {
  return `요지 보기:
- 정답: 필자 최종 메시지 한 문장 (소재 소개 X).
- 강한 오답: 부분 사실(참이지만 요지 아님) / 관계만 미세 왜곡.
- 오답: 반대·통념·사례 일반화.
- ${en ? "영어 완전문장 paraphrase" : "시험체 한국어"}.`;
}

export function impliedMeaningChoiceCraft(): string {
  return `함축의미 보기:
- 정답: 사전·직역 X → 문맥 함축 paraphrase.
- 강한 오답: 문자 그대로 / 지나치게 넓음 / 원인·결과 바꿈 / 주변 문맥만 반영.
- 오답도 밑줄 표현의 일부 의미를 포함해 매력적으로.
- 예) arrow…reverse → ○ behaviors can shape beliefs as much as beliefs shape behavior
  × "do two things"류 일반론.`;
}

export function contentFalseChoiceCraft(en: boolean): string {
  return `내용일치·불일치 보기:
- 모든 선택지 = 원문 구체 내용의 자연스러운 paraphrase (원문 복사 금지).
- 틀린 것(불일치)도 원문 단어를 활용하되 주체·방향·정도·조건·인과 중 하나만 미세 변경.
- ‘언급되지 않음’만으로 오답 금지.
- ${en ? "영어" : "한국어"} 시험체.`;
}

export function vocabChoiceCraft(): string {
  return `어휘(문맥) 밑줄:
- 틀린 어휘: 그 문장만 보면 자연스러워 보이게. 앞뒤·전체 논리 방향을 봐야 오류 발견.
- 너무 명백한 반의어·주제 무관 단어 금지.
- 나머지 밑줄은 문법·문맥 완전 적합.
- 예시는 방향 반전(favor→dislike)이되, 문장 단독으로는 ‘가능해 보이는’ 수준.`;
}

export function irrelevantChoiceCraft(): string {
  return `무관한 문장:
- 소재·어휘는 지문과 유사해 보이게.
- 논리 전개에는 기여하지 않음 (강한 오답처럼 표면 연결).
- 완전 황당·딴 세계 소재 금지. 원문 핵심문을 무관으로 지정 금지.`;
}

export function insertionChoiceCraft(): string {
  return `문장 삽입:
- 삽입문에 대명사·지시·연결·반복·인과 단서 ≥1.
- 정답 위치에서만 자연. 두 자리 가능하면 재선정.
- 위치가 ‘명백히 하나만’ 보이도록 하되, 인접 슬롯이 약하게 매력적이게(강한 오답 위치).`;
}

export function grammarChoiceCraftNote(): string {
  return `어법 보기·밑줄 추가:
- 틀린 곳 제외한 밑줄은 실제로 완전 올바름.
- 단순 인접 수일치·철자 트릭 금지. 문장 구조 이해 필요 (관계사·준동사·병렬·태·시제·비교 등).
- 문법적으로 맞지만 낯설어 보이는 표현을 정답처럼 유인하는 밑줄로 배치.
- 의도치 않은 추가 오류 없는지 전체 재검토.`;
}
